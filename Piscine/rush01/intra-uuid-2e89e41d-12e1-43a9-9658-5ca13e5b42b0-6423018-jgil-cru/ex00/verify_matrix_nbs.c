int	verify_matrix_nbs(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (i % 2 == 0)
		{
			if (!(str[i] >= '1' && str[i] <= '4'))
				return (0);
		}
		else if (i % 2 != 0)
		{
			if (!(str[i] == ' '))
				return (0);
		}
		i++;
	}
	return (1);
}
